function plotEpochDuration(runGroupName, chartId, tickOptions = null) {
    let labels = Object.keys(EPOCH_MINUTES[runGroupName])
        .sort((a, b) => parseInt(a) - parseInt(b));
    let values = labels.map(l => EPOCH_MINUTES[runGroupName][l]);
    let colors = labels.map(l => RUN_GROUP_COLORS[runGroupName][l]);

    let chartOptions = {
        scales: {
            xAxes: [{
                scaleLabel: {
                    display: true,
                    labelString: 'Minutes per Epoch'
                },
                ticks: {
                    min: 0,
                }
            }]
        },
        legend: {
            display: false
        }
    };
    if (tickOptions) {
        $.extend(chartOptions.scales.xAxes[0].ticks, tickOptions);
    }

    new Chart(chartId, {
        type: 'horizontalBar',
        data: {
            labels: labels,
            datasets: [{
                data: values,
                borderColor: colors,
                backgroundColor: colors
            }]
        },
        options: chartOptions
    });
}